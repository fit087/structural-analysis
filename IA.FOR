************************************************************************
*                 TRANSFORMAÇÃO DE INTEIRO EM CHARACTER                *
************************************************************************

*-----------------------------------------------------------------------
      SUBROUTINE IA (N,A2)
*-----------------------------------------------------------------------

	IMPLICIT NONE
      INTEGER I,J,K,N
	CHARACTER*2 A2
      I=N
	K=2
	A2=''
	DO WHILE (I>0)
	J=I-10*(INT(I/10.))
	A2(K:K)=CHAR(J+48)
	I=INT(I/10)
	K=K-1
	END DO

      END